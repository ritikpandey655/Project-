
export enum ExamType {
  UPSC = 'UPSC',
  SSC_CGL = 'SSC CGL',
  JEE_MAINS = 'JEE Mains',
  NEET = 'NEET',
  BANKING = 'Banking',
  RAILWAYS = 'Railways',
  GATE = 'GATE'
}

export enum QuestionSource {
  PYQ_AI = 'PYQ_AI', // Generated by AI simulating PYQs
  USER = 'USER'      // Uploaded by user for revision
}

export interface Question {
  id: string;
  text: string;
  options: string[];
  correctIndex: number;
  explanation?: string;
  source: QuestionSource;
  examType: ExamType; // e.g., 'SSC CGL'
  subject?: string;
  createdAt: number;
  tags?: string[];
}

export interface UserStats {
  totalAttempted: number;
  totalCorrect: number;
  streakCurrent: number;
  streakMax: number;
  lastActiveDate: string; // ISO date string YYYY-MM-DD
  subjectPerformance: Record<string, { correct: number; total: number }>;
  examPerformance?: Record<string, {
    totalAttempted: number;
    totalCorrect: number;
    subjectPerformance: Record<string, { correct: number; total: number }>;
  }>;
}

export interface User {
  id: string;
  name: string;
  email: string;
  photoURL?: string;
}

export type ViewState = 'login' | 'signup' | 'onboarding' | 'tutorial' | 'dashboard' | 'practice' | 'upload' | 'stats';

export interface AppState {
  view: ViewState;
  selectedExam: ExamType | null;
  stats: UserStats;
  user: User | null;
  showTimer: boolean;
}
